public class IndexExtremeInfo {
    private String clientType;
    private String token;
    private String storeId;
    private String contractAmt;

    public IndexExtremeInfo() {
    }

    public IndexExtremeInfo(String clientType, String contractAmt, String storeId, String token) {
        this.clientType = clientType;
        this.contractAmt = contractAmt;
        this.storeId = storeId;
        this.token = token;
    }

    public String getClientType() {
        return clientType;
    }

    public void setClientType(String clientType) {
        this.clientType = clientType;
    }

    public String getContractAmt() {
        return contractAmt;
    }

    public void setContractAmt(String contractAmt) {
        this.contractAmt = contractAmt;
    }

    public String getStoreId() {
        return storeId;
    }

    public void setStoreId(String storeId) {
        this.storeId = storeId;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    @Override
    public String toString() {
        return "IndexExtremeInfo{" +
                "clientType='" + clientType + '\'' +
                ", token='" + token + '\'' +
                ", storeId='" + storeId + '\'' +
                ", contractAmt='" + contractAmt + '\'' +
                '}';
    }
}
