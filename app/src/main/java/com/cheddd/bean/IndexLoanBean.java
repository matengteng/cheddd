public class IndexLoanBean {
    private String clientType;
    private String token;
    private int contractAmt;
    private int interestType;
    private int loanCycle;

    public IndexLoanBean() {
    }

    public IndexLoanBean(String clientType, String token, int contractAmt, int interestType, int loanCycle) {
        this.clientType = clientType;
        this.token = token;
        this.contractAmt = contractAmt;
        this.interestType = interestType;
        this.loanCycle = loanCycle;
    }

    public String getClientType() {
        return clientType;
    }

    public void setClientType(String clientType) {
        this.clientType = clientType;
    }

    public int getLoanCycle() {
        return loanCycle;
    }

    public void setLoanCycle(int loanCycle) {
        this.loanCycle = loanCycle;
    }

    public int getInterestType() {
        return interestType;
    }

    public void setInterestType(int interestType) {
        this.interestType = interestType;
    }

    public int getContractAmt() {
        return contractAmt;
    }

    public void setContractAmt(int contractAmt) {
        this.contractAmt = contractAmt;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    @Override
    public String toString() {
        return "IndexLoanBean{" +
                "clientType='" + clientType + '\'' +
                ", token='" + token + '\'' +
                ", contractAmt='" + contractAmt + '\'' +
                ", interestType='" + interestType + '\'' +
                ", loanCycle='" + loanCycle + '\'' +
                '}';
    }
}
